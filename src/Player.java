public class Player{
	 String name;
	 int money;
	 String symbol;
	 
	public Player(String name,String symbol) {
		this.name = name;
		this.symbol=symbol;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	
	 
	 
}



